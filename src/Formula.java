public class Formula {
      public double base;
      public double heigth;

      public Formula(double base, double heigth) {
            this.base = base;
            this.heigth = heigth;
      }

      public double getBase() {
            return base;
      }

      public double getHeigth() {
            return heigth;
      }

      public void setBase(double base) {
            this.base = base;
      }

      public void setHeigth(double heigth) {
            this.heigth = heigth;
      }
      
      
}
